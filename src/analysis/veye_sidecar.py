"""Reader for the .veblk block-analysis sidecar produced by veye_probe.

The sidecar is generated by the patched-FFmpeg helper (native/veye_probe.c).
It carries per-frame per-macroblock coding info (mb_type, QP) that mainline
FFmpeg does not expose. This module parses it and converts the codec
MB_TYPE bitmask into the codec-agnostic BLOCK_DTYPE partitions consumed by
the overlays.
"""

from __future__ import annotations

import os
import struct
from dataclasses import dataclass
from typing import Optional

import numpy as np

from .schema import BLOCK_DTYPE, MV_DTYPE, PredType

# Sidecar file header: u32 magic 'VEYE', u32 version, u32 n_frames
_FILE_MAGIC = 0x45594556  # bytes V E Y E
_FILE_HDR = struct.Struct("<III")
_ENTRY_HDR = struct.Struct("<II")  # frame_index, payload_size

# VeyeBlockInfoHeader: u32 magic 'VEBK', u16 ver, u16 rec_size, u32 codec_id,
#                      u32 grid_w, u32 grid_h, u32 block_unit, u32 n_records
_BLK_MAGIC = 0x4B424556  # bytes V E B K
_BLK_HDR = struct.Struct("<IHHIIIII")
# VeyeBlockRecord (H.264): u32 mb_type, i32 qp
_REC = np.dtype([("mb_type", "<u4"), ("qp", "<i4")])
# VeyeBlockRecordHEVC (sidecar v2): u8 cu_log2, u8 pred, u8 intra_mode,
# u8 pred_flag, i32 qp, i16 mv0_x/y, i16 mv1_x/y, i8 ref0/ref1, u16 reserved.
_REC_HEVC = np.dtype([
    ("cu_log2", "u1"), ("pred", "u1"), ("intra_mode", "u1"),
    ("pred_flag", "u1"), ("qp", "<i4"),
    ("mv0_x", "<i2"), ("mv0_y", "<i2"), ("mv1_x", "<i2"), ("mv1_y", "<i2"),
    ("ref0", "i1"), ("ref1", "i1"), ("part_mode", "u1"), ("reserved", "u1"),
])

# VeyeBlockRecordAV1 (sidecar v2): u8 bsize, pred, mode, skip, i32 qp,
# i16 mv0_x/y, i16 mv1_x/y, i8 ref0/ref1, u8 tx_size, u8 reserved (20 bytes).
_REC_AV1 = np.dtype([
    ("bsize", "u1"), ("pred", "u1"), ("mode", "u1"), ("skip", "u1"),
    ("qp", "<i4"),
    ("mv0_x", "<i2"), ("mv0_y", "<i2"), ("mv1_x", "<i2"), ("mv1_y", "<i2"),
    ("ref0", "i1"), ("ref1", "i1"), ("tx_size", "u1"), ("reserved", "u1"),
])

# HEVC motion vectors are stored in quarter-pel luma units.
_HEVC_MV_SCALE = 4.0
# AV1 motion vectors are stored in eighth-pel luma units.
_AV1_MV_SCALE = 8.0

# codec_id values written into the header (AVCodecID, ABI-stable in practice).
_CODEC_H264 = 27
_CODEC_HEVC = 173
_CODEC_AV1 = 225

# libaom BLOCK_SIZE enum -> (luma width, height) px, indexed 0..BLOCK_SIZES_ALL-1.
_AV1_BSIZE_WH = (
    (4, 4), (4, 8), (8, 4), (8, 8), (8, 16), (16, 8), (16, 16), (16, 32),
    (32, 16), (32, 32), (32, 64), (64, 32), (64, 64), (64, 128), (128, 64),
    (128, 128), (4, 16), (16, 4), (8, 32), (32, 8), (16, 64), (64, 16),
)

# FFmpeg MB_TYPE_* bits (libavcodec/mpegutils.h) — ABI-stable.
MB_TYPE_INTRA4x4 = 1 << 0
MB_TYPE_INTRA16x16 = 1 << 1
MB_TYPE_INTRA_PCM = 1 << 2
MB_TYPE_16x16 = 1 << 3
MB_TYPE_16x8 = 1 << 4
MB_TYPE_8x16 = 1 << 5
MB_TYPE_8x8 = 1 << 6
MB_TYPE_DIRECT2 = 1 << 8
MB_TYPE_P0L0 = 1 << 12
MB_TYPE_P1L0 = 1 << 13
MB_TYPE_P0L1 = 1 << 14
MB_TYPE_P1L1 = 1 << 15
MB_TYPE_SKIP = 1 << 17

_MB_TYPE_INTRA = MB_TYPE_INTRA4x4 | MB_TYPE_INTRA16x16 | MB_TYPE_INTRA_PCM
_MB_TYPE_L0 = MB_TYPE_P0L0 | MB_TYPE_P1L0
_MB_TYPE_L1 = MB_TYPE_P0L1 | MB_TYPE_P1L1

# Partition shape codes stored in BLOCK_DTYPE["mode"] for labeling.
SHAPE_16x16, SHAPE_16x8, SHAPE_8x16, SHAPE_8x8 = 0, 1, 2, 3
SHAPE_INTRA16, SHAPE_INTRA4, SHAPE_IPCM, SHAPE_SKIP, SHAPE_DIRECT = 4, 5, 6, 7, 8


@dataclass
class VeyeFrameBlocks:
    """Raw per-cell grid for one frame.

    H.264 cells are 16x16 macroblocks carrying an mb_type bitmask. HEVC
    cells are min coding blocks (typically 8x8); the variable CU size and
    prediction class live in the cu_log2/pred/intra_mode grids instead.
    """
    codec_id: int
    grid_w: int
    grid_h: int
    block_unit: int
    qp: np.ndarray                          # int32, shape (grid_h, grid_w)
    mb_type: Optional[np.ndarray] = None    # H.264: uint32 grid
    cu_log2: Optional[np.ndarray] = None    # HEVC: log2 CU size px
    pred: Optional[np.ndarray] = None       # HEVC: PredType per cell
    intra_mode: Optional[np.ndarray] = None  # HEVC: luma intra mode 0..34
    pred_flag: Optional[np.ndarray] = None  # HEVC: PredFlag (0/1/2/3 = I/L0/L1/BI)
    part_mode: Optional[np.ndarray] = None  # HEVC: PartMode per cell (0..7)
    mv: Optional[np.ndarray] = None         # HEVC/AV1: int16 grid (h, w, 2 lists, 2 xy)
    ref_idx: Optional[np.ndarray] = None    # HEVC/AV1: int8 grid (h, w, 2 lists)
    bsize: Optional[np.ndarray] = None      # AV1: BLOCK_SIZE enum per 4x4 MI cell
    mode: Optional[np.ndarray] = None       # AV1: PREDICTION_MODE per cell
    skip: Optional[np.ndarray] = None       # AV1: skip_txfm flag per cell
    tx_size: Optional[np.ndarray] = None    # AV1: TX_SIZE per cell


def load_sidecar(path: str) -> Optional[dict[int, VeyeFrameBlocks]]:
    """Load a .veblk file into {frame_index: VeyeFrameBlocks}, or None."""
    try:
        with open(path, "rb") as f:
            data = f.read()
    except OSError:
        return None
    if len(data) < _FILE_HDR.size:
        return None
    magic, _ver, n_frames = _FILE_HDR.unpack_from(data, 0)
    if magic != _FILE_MAGIC or n_frames <= 0:
        return None  # bad magic, or header not finalised (n_frames still 0)

    frames: dict[int, VeyeFrameBlocks] = {}
    off = _FILE_HDR.size
    for _ in range(n_frames):
        # Any truncation means the file is incomplete (e.g. an interrupted or
        # still-running helper): reject it so the caller regenerates rather
        # than trusting a partial frame set.
        if off + _ENTRY_HDR.size > len(data):
            return None
        frame_index, payload_size = _ENTRY_HDR.unpack_from(data, off)
        off += _ENTRY_HDR.size
        if off + payload_size > len(data):
            return None
        payload = data[off:off + payload_size]
        off += payload_size
        fb = _parse_payload(payload)
        if fb is not None:
            frames[frame_index] = fb
    return frames if frames else None


FILE_HEADER_SIZE = _FILE_HDR.size


def read_incremental(
    path: str, offset: int, header_ok: bool
) -> tuple[dict[int, VeyeFrameBlocks], int, bool]:
    """Parse frame entries appended to a .veblk that is still being written.

    Returns (new_frames, new_offset, header_ok). Only *complete* entries are
    decoded: the helper writes the file as a single sequential append, so the
    bytes on disk are always a valid prefix of the final stream, truncated at
    some byte. We stop at the first entry whose header or payload is not fully
    present yet and resume from there on the next call. The header's n_frames
    field is ignored (it stays 0 until the helper finishes); we read to EOF.

    `offset` is the number of bytes already consumed into complete entries
    (FILE_HEADER_SIZE once the header is validated). All file access is
    defensive: a missing file, a sharing violation, or a not-yet-written
    header simply yields no new frames and is retried later.
    """
    frames: dict[int, VeyeFrameBlocks] = {}
    try:
        size = os.path.getsize(path)
    except OSError:
        return frames, offset, header_ok

    if not header_ok:
        if size < _FILE_HDR.size:
            return frames, offset, False
        try:
            with open(path, "rb") as f:
                hdr = f.read(_FILE_HDR.size)
        except OSError:
            return frames, offset, False
        if len(hdr) < _FILE_HDR.size:
            return frames, offset, False
        magic, _ver, _n = _FILE_HDR.unpack(hdr)
        if magic != _FILE_MAGIC:
            return frames, offset, False
        header_ok = True
        offset = _FILE_HDR.size

    if size <= offset:
        return frames, offset, header_ok

    try:
        with open(path, "rb") as f:
            f.seek(offset)
            data = f.read()
    except OSError:
        return frames, offset, header_ok

    off, n = 0, len(data)
    while off + _ENTRY_HDR.size <= n:
        frame_index, payload_size = _ENTRY_HDR.unpack_from(data, off)
        end = off + _ENTRY_HDR.size + payload_size
        if end > n:
            break  # payload not fully written yet
        fb = _parse_payload(data[off + _ENTRY_HDR.size:end])
        if fb is not None:
            frames[frame_index] = fb
        off = end

    return frames, offset + off, header_ok


def _parse_payload(payload: bytes) -> Optional[VeyeFrameBlocks]:
    if len(payload) < _BLK_HDR.size:
        return None
    (magic, _ver, rec_size, codec_id,
     grid_w, grid_h, block_unit, n_records) = _BLK_HDR.unpack_from(payload, 0)
    if magic != _BLK_MAGIC or grid_w == 0 or grid_h == 0:
        return None
    if n_records != grid_w * grid_h:
        return None

    if codec_id == _CODEC_HEVC:
        recs = np.frombuffer(payload, dtype=_REC_HEVC, count=n_records,
                             offset=_BLK_HDR.size)
        mv = np.empty((grid_h, grid_w, 2, 2), dtype=np.int16)
        mv[..., 0, 0] = recs["mv0_x"].reshape(grid_h, grid_w)
        mv[..., 0, 1] = recs["mv0_y"].reshape(grid_h, grid_w)
        mv[..., 1, 0] = recs["mv1_x"].reshape(grid_h, grid_w)
        mv[..., 1, 1] = recs["mv1_y"].reshape(grid_h, grid_w)
        ref = np.empty((grid_h, grid_w, 2), dtype=np.int8)
        ref[..., 0] = recs["ref0"].reshape(grid_h, grid_w)
        ref[..., 1] = recs["ref1"].reshape(grid_h, grid_w)
        return VeyeFrameBlocks(
            codec_id, grid_w, grid_h, block_unit,
            qp=recs["qp"].reshape(grid_h, grid_w).copy(),
            cu_log2=recs["cu_log2"].reshape(grid_h, grid_w).copy(),
            pred=recs["pred"].reshape(grid_h, grid_w).copy(),
            intra_mode=recs["intra_mode"].reshape(grid_h, grid_w).copy(),
            pred_flag=recs["pred_flag"].reshape(grid_h, grid_w).copy(),
            part_mode=recs["part_mode"].reshape(grid_h, grid_w).copy(),
            mv=mv, ref_idx=ref,
        )

    if codec_id == _CODEC_AV1:
        recs = np.frombuffer(payload, dtype=_REC_AV1, count=n_records,
                             offset=_BLK_HDR.size)
        mv = np.empty((grid_h, grid_w, 2, 2), dtype=np.int16)
        mv[..., 0, 0] = recs["mv0_x"].reshape(grid_h, grid_w)
        mv[..., 0, 1] = recs["mv0_y"].reshape(grid_h, grid_w)
        mv[..., 1, 0] = recs["mv1_x"].reshape(grid_h, grid_w)
        mv[..., 1, 1] = recs["mv1_y"].reshape(grid_h, grid_w)
        ref = np.empty((grid_h, grid_w, 2), dtype=np.int8)
        ref[..., 0] = recs["ref0"].reshape(grid_h, grid_w)
        ref[..., 1] = recs["ref1"].reshape(grid_h, grid_w)
        return VeyeFrameBlocks(
            codec_id, grid_w, grid_h, block_unit,
            qp=recs["qp"].reshape(grid_h, grid_w).copy(),
            pred=recs["pred"].reshape(grid_h, grid_w).copy(),
            bsize=recs["bsize"].reshape(grid_h, grid_w).copy(),
            mode=recs["mode"].reshape(grid_h, grid_w).copy(),
            skip=recs["skip"].reshape(grid_h, grid_w).copy(),
            tx_size=recs["tx_size"].reshape(grid_h, grid_w).copy(),
            mv=mv, ref_idx=ref,
        )

    recs = np.frombuffer(payload, dtype=_REC, count=n_records,
                         offset=_BLK_HDR.size)
    return VeyeFrameBlocks(
        codec_id, grid_w, grid_h, block_unit,
        qp=recs["qp"].reshape(grid_h, grid_w).copy(),
        mb_type=recs["mb_type"].reshape(grid_h, grid_w).copy(),
    )


def _pack(out: list, dtype) -> np.ndarray:
    """Build a structured array from a list of record tuples in one C-level
    conversion, instead of allocating and filling element-by-element."""
    if not out:
        return np.empty(0, dtype=dtype)
    return np.array(out, dtype=dtype)


def blocks_from_frame(fb: VeyeFrameBlocks) -> np.ndarray:
    """Decode a frame's per-cell grid into BLOCK_DTYPE partitions."""
    if fb.codec_id == _CODEC_HEVC:
        return _blocks_from_hevc(fb)
    if fb.codec_id == _CODEC_AV1:
        return _blocks_from_av1(fb)

    unit = fb.block_unit
    out: list[tuple] = []
    mb = fb.mb_type
    for my in range(fb.grid_h):
        for mx in range(fb.grid_w):
            t = int(mb[my, mx])
            bx, by = mx * unit, my * unit
            for x, y, w, h, shape, pred in _partition(t, bx, by, unit):
                out.append((x, y, w, h, 0, pred, shape))
    return _pack(out, BLOCK_DTYPE)


def _blocks_from_hevc(fb: VeyeFrameBlocks) -> np.ndarray:
    """Collapse the min-CB grid back into the HEVC coding-unit quadtree.

    Every min-CB cell carries the log2 size of the CU it belongs to. A CU
    is aligned to its own size, so the cell at the CU's top-left corner is
    the unique cell where both pixel coordinates are multiples of the CU
    size; we emit one partition rectangle there. The mode field stores
    cu_log2 for labeling.
    """
    unit = fb.block_unit
    cu_log2 = fb.cu_log2
    pred = fb.pred
    out: list[tuple] = []
    for my in range(fb.grid_h):
        py = my * unit
        for mx in range(fb.grid_w):
            cl = int(cu_log2[my, mx])
            cu_px = 1 << cl
            px = mx * unit
            if px % cu_px or py % cu_px:
                continue  # not the top-left cell of this CU
            out.append((px, py, cu_px, cu_px, 0, int(pred[my, mx]), cl))
    return _pack(out, BLOCK_DTYPE)


def _blocks_from_av1(fb: VeyeFrameBlocks) -> np.ndarray:
    """Collapse the 4x4 MI grid into AV1 coding blocks.

    ifd_inspect replicates the same mode-info across every 4x4 cell a coding
    block covers. Each AV1 block is aligned to its own (w, h), so the cell at
    the block's top-left corner is the unique cell whose pixel coordinates are
    multiples of the block dimensions; we emit one rectangle there. The mode
    field stores the libaom PREDICTION_MODE for labeling.
    """
    unit = fb.block_unit
    bsize = fb.bsize
    pred = fb.pred
    mode = fb.mode
    out: list[tuple] = []
    for my in range(fb.grid_h):
        py = my * unit
        for mx in range(fb.grid_w):
            bs = int(bsize[my, mx])
            if bs < 0 or bs >= len(_AV1_BSIZE_WH):
                continue
            bw, bh = _AV1_BSIZE_WH[bs]
            px = mx * unit
            if px % bw or py % bh:
                continue  # not the top-left cell of this block
            out.append((px, py, bw, bh, 0, int(pred[my, mx]), int(mode[my, mx])))
    return _pack(out, BLOCK_DTYPE)


# HEVC PartMode -> PU rectangles as (fx, fy, fw, fh) fractions of the CU.
_PART_PUS = {
    0: ((0.0, 0.0, 1.0, 1.0),),                                      # 2Nx2N
    1: ((0.0, 0.0, 1.0, 0.5), (0.0, 0.5, 1.0, 0.5)),                 # 2NxN
    2: ((0.0, 0.0, 0.5, 1.0), (0.5, 0.0, 0.5, 1.0)),                 # Nx2N
    3: ((0.0, 0.0, 0.5, 0.5), (0.5, 0.0, 0.5, 0.5),
        (0.0, 0.5, 0.5, 0.5), (0.5, 0.5, 0.5, 0.5)),                 # NxN
    4: ((0.0, 0.0, 1.0, 0.25), (0.0, 0.25, 1.0, 0.75)),             # 2NxnU
    5: ((0.0, 0.0, 1.0, 0.75), (0.0, 0.75, 1.0, 0.25)),             # 2NxnD
    6: ((0.0, 0.0, 0.25, 1.0), (0.25, 0.0, 0.75, 1.0)),             # nLx2N
    7: ((0.0, 0.0, 0.75, 1.0), (0.75, 0.0, 0.25, 1.0)),             # nRx2N
}


def pus_from_frame(fb: VeyeFrameBlocks) -> np.ndarray:
    """HEVC prediction units (BLOCK_DTYPE rectangles) from CU size + part_mode.

    Each CU (located at its top-left min-CB cell) is split into PUs per its
    PartMode. For 2Nx2N the single PU coincides with the CU.
    """
    if (fb.codec_id != _CODEC_HEVC or fb.cu_log2 is None
            or fb.part_mode is None):
        return np.empty(0, dtype=BLOCK_DTYPE)
    unit = fb.block_unit
    cu_log2 = fb.cu_log2
    pm = fb.part_mode
    pred = fb.pred
    out: list[tuple] = []
    for my in range(fb.grid_h):
        py = my * unit
        for mx in range(fb.grid_w):
            cl = int(cu_log2[my, mx])
            cu_px = 1 << cl
            px = mx * unit
            if px % cu_px or py % cu_px:
                continue  # not the CU's top-left cell
            mode = int(pm[my, mx])
            p = int(pred[my, mx]) if pred is not None else 0
            for fx, fy, fw, fh in _PART_PUS.get(mode, _PART_PUS[0]):
                out.append((px + int(fx * cu_px), py + int(fy * cu_px),
                            int(fw * cu_px), int(fh * cu_px), 0, p, mode))
    return _pack(out, BLOCK_DTYPE)


def qp_grid_from_frame(fb: VeyeFrameBlocks) -> Optional[np.ndarray]:
    """QP grid at block_unit-pixel granularity (int16, -1 = unknown)."""
    if fb.qp is None:
        return None
    return fb.qp.astype(np.int16)


def mvs_from_frame(fb: VeyeFrameBlocks) -> np.ndarray:
    """Motion vectors (MV_DTYPE) for one frame, one per prediction region."""
    if fb.codec_id == _CODEC_AV1:
        return _mvs_from_av1(fb)
    return _mvs_from_hevc(fb)


def _mvs_from_av1(fb: VeyeFrameBlocks) -> np.ndarray:
    """Motion vectors for an AV1 frame, collapsed per coding block.

    Each inter block emits an L0 vector (ref_frame[0] >= 1) and, for compound
    prediction, an L1 vector (ref_frame[1] >= 1). ref_frame == 0 is INTRA_FRAME
    (intra block, no motion). Vectors are converted from eighth-pel to pixels.
    """
    if fb.bsize is None or fb.mv is None or fb.ref_idx is None:
        return np.empty(0, dtype=MV_DTYPE)
    unit = fb.block_unit
    bsize = fb.bsize
    ref = fb.ref_idx
    mv = fb.mv
    out: list[tuple] = []
    for my in range(fb.grid_h):
        py = my * unit
        for mx in range(fb.grid_w):
            bs = int(bsize[my, mx])
            if bs < 0 or bs >= len(_AV1_BSIZE_WH):
                continue
            bw, bh = _AV1_BSIZE_WH[bs]
            px = mx * unit
            if px % bw or py % bh:
                continue  # not the block's top-left cell
            if int(ref[my, mx, 0]) >= 1:  # 0 = INTRA_FRAME
                out.append((px, py, bw, bh, 0,
                            mv[my, mx, 0, 0] / _AV1_MV_SCALE,
                            mv[my, mx, 0, 1] / _AV1_MV_SCALE))
            if int(ref[my, mx, 1]) >= 1:  # compound L1
                out.append((px, py, bw, bh, 1,
                            mv[my, mx, 1, 0] / _AV1_MV_SCALE,
                            mv[my, mx, 1, 1] / _AV1_MV_SCALE))
    return _pack(out, MV_DTYPE)


def _mvs_from_hevc(fb: VeyeFrameBlocks) -> np.ndarray:
    """Motion vectors (MV_DTYPE) for an HEVC frame, one per prediction region.

    The motion field is sampled per min-CB. Cells are collapsed to their CU
    when the whole CU shares one MvField (the 2Nx2N case); CUs whose sub-cells
    differ (2NxN/Nx2N/NxN partitions) emit one vector per min-CB so the split
    is preserved. Vectors are converted from quarter-pel to luma pixels.
    """
    if fb.codec_id != _CODEC_HEVC or fb.pred_flag is None or fb.mv is None:
        return np.empty(0, dtype=MV_DTYPE)

    unit = fb.block_unit
    cu_log2 = fb.cu_log2
    pf = fb.pred_flag
    mv = fb.mv
    out: list[tuple] = []

    def emit(px, py, size, flag, m):
        if flag & 1:
            out.append((px, py, size, size, 0,
                        m[0, 0] / _HEVC_MV_SCALE, m[0, 1] / _HEVC_MV_SCALE))
        if flag & 2:
            out.append((px, py, size, size, 1,
                        m[1, 0] / _HEVC_MV_SCALE, m[1, 1] / _HEVC_MV_SCALE))

    for my in range(fb.grid_h):
        py = my * unit
        for mx in range(fb.grid_w):
            cl = int(cu_log2[my, mx])
            cu_px = 1 << cl
            px = mx * unit
            if px % cu_px or py % cu_px:
                continue  # not the CU's top-left cell
            span = cu_px // unit
            y1 = min(my + span, fb.grid_h)
            x1 = min(mx + span, fb.grid_w)
            sub_pf = pf[my:y1, mx:x1]
            if not sub_pf.any():
                continue  # intra CU, no motion
            sub_mv = mv[my:y1, mx:x1]
            uniform = (bool((sub_pf == sub_pf.flat[0]).all())
                       and bool((sub_mv == sub_mv[0, 0]).all()))
            if uniform:
                emit(px, py, cu_px, int(sub_pf.flat[0]), sub_mv[0, 0])
            else:
                for dy in range(y1 - my):
                    for dx in range(x1 - mx):
                        f = int(sub_pf[dy, dx])
                        if f:
                            emit(px + dx * unit, py + dy * unit, unit,
                                 f, sub_mv[dy, dx])

    return _pack(out, MV_DTYPE)


def _partition(t: int, bx: int, by: int, u: int):
    """Yield (x, y, w, h, shape, pred) sub-blocks for one macroblock."""
    if t & MB_TYPE_INTRA_PCM:
        yield (bx, by, u, u, SHAPE_IPCM, PredType.IPCM)
        return
    if t & _MB_TYPE_INTRA:
        shape = SHAPE_INTRA4 if (t & MB_TYPE_INTRA4x4) else SHAPE_INTRA16
        yield (bx, by, u, u, shape, PredType.INTRA)
        return
    pred = PredType.SKIP if (t & MB_TYPE_SKIP) else PredType.INTER
    if t & MB_TYPE_16x8:
        yield (bx, by, u, u // 2, SHAPE_16x8, pred)
        yield (bx, by + u // 2, u, u // 2, SHAPE_16x8, pred)
    elif t & MB_TYPE_8x16:
        yield (bx, by, u // 2, u, SHAPE_8x16, pred)
        yield (bx + u // 2, by, u // 2, u, SHAPE_8x16, pred)
    elif t & MB_TYPE_8x8:
        h = u // 2
        for dy in (0, h):
            for dx in (0, h):
                yield (bx + dx, by + dy, h, h, SHAPE_8x8, pred)
    else:
        shape = SHAPE_SKIP if (t & MB_TYPE_SKIP) else (
            SHAPE_DIRECT if (t & MB_TYPE_DIRECT2) else SHAPE_16x16)
        yield (bx, by, u, u, shape, pred)
